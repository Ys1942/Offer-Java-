/**一只青蛙一次可以跳上1级台阶，也可以跳上2级。
 * 求该青蛙跳上一个n级的台阶总共有多少种跳法
 * （先后次序不同算不同的结果）。
 * 本质上是斐波那契数列
 */
public class Main8 {
	public int JumpFloor(int target) {
		if(target<3)
			return target;
		int f1 = 1;
        int f2 = 2;
		int fn = 0;
        for(int i =3;i<=target;i++){
            fn = f1 + f2;
            f1 = f2;
            f2 = fn;
        }
        return fn;
    }
}
